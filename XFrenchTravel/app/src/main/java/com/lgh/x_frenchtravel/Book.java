package com.lgh.x_frenchtravel;

public class Book {

    public String username;
    public String hotelName;
    public String fullDate;
    public String peoples;
    public String anotherInfos;
    public String price;

    public Book(String username, String hotelName, String fullDate, String peoples, String anotherInfos, String price) {
        this.username = username;
        this.hotelName = hotelName;
        this.fullDate = fullDate;
        this.peoples = peoples;
        this.anotherInfos = anotherInfos;
        this.price = price;
    }

}
